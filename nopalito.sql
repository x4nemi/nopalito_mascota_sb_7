CREATE TABLE lugar(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	codigo_postal MEDIUMINT UNSIGNED NOT NULL,
    municipio VARCHAR(100) NOT NULL,
    colonia VARCHAR(100) NOT NULL,
    calle VARCHAR(100) NOT NULL
