);

CREATE TABLE arbol(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    fecha DATE NOT NULL,
    especie VARCHAR(100) NOT NULL,
    imagen VARCHAR(1024) NOT NULL,
    lugarId INT UNSIGNED NOT NULL,
    FOREIGN KEY (lugarId) REFERENCES lugar(id)
);